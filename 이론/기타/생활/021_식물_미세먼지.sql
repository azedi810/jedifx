뿌리가 뻗어나가면서 벽이나 철사를 타고 올라가는 특성이 있습니다
- 직광에는 잎이 탈수가 있깅 ㅔ반양지가 적합하며 물도 어느정도 좋아하기에 
  절절한 수분공급에 통풍이 잘되면 폭풍성장합니다
- 잎에 독소가 있기에 어린아이가 있거나 반려동물이 있으면 주의하세요 
- 수경재배로도 키울 수 있는 식물이니 물주기가 어려운 분은 수경재배로도 추천


