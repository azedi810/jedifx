/*****************************************************
* 2. 관음죽
******************************************************/
관음죽은 낮은 기온에서도 아랑곳않는 내한성을 자랑하고, 병충해에도 강한 식물입니다. 
암모니아를 잘 흡수하는 특징이 있어서 주로 화장실에 배치하면 좋습니다. 
포름알데히르까지 제거해 새집증후군에도 도움이 됩니다. 
아레카야자와 마찬가지로 제습 식물 로도 좋습니다. 
인테리어 용으로는 갈색계열의 고가구와 잘 어울립니다.
2. 잘 크지 않는다. 성장 속도 느리다 

/*****************************************************
* 3. 대나무야자
******************************************************/
대나무야자 역시 아레카야자와 마찬가지로 실내 습도를 올리는 데 매우 효과적이라고 합니다. 
또한 마찬가지로 직사광선을 받으면 잎이 타기 때문에 양지와 음지가 적당히 조화된 곳에서 기르시는 것이 좋습니다. 
1m 이상의 대나무야자를 커다란 토분에 심어 TV 옆이나 소파 뒤쪽에 배치하면 실내 공기 질을 높이는 건 물론, 
이국적인 인테리어를 완성할 수 있습니다.


/*****************************************************
* 4. 인도고무나무
******************************************************/
잎이 넓은 인도고무나무는 머리를 맑게 해주고 공기 중의 유해물질을 잘 흡수합니다. 
관리 또한 쉬워서 누구나 쉽게 키울 수 있는 식물입니다.

/*****************************************************
* 5. 드라세나 자넷 크레이그
******************************************************/
드라세나 자넷 크레이그는 트리클로로 에틸렌이라는 휘발성 화학물질을 제거하는 뛰어난 능력을 가지고 있어서 
밀폐된 사무실같은 공간에 두시면 좋습니다. 
물이 부족하면 잎이 갈색으로 변하니 물 조절을 잘 하셔야 되며, 
그늘진 곳에 두면 물의 양을 양지에서보다 조금 더 적게 주시면 됩니다.

/*****************************************************
* 6.아이비
******************************************************/
공기청정기에 들어가는 헤파필터도 잡아내지 못하는 유해물질을 막아줄 수 있는 유전자 변형 식물
